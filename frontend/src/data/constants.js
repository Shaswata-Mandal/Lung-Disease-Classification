export function getDiseaseStyle(prediction = '') {

  const p = prediction.toLowerCase();

  if (p.includes('covid') || p.includes('corona')) return { color: 'text-red-400', emoji: '🦠', hex: '#f87171' };
  if (p.includes('pneumonia')) return { color: 'text-yellow-400', emoji: '🌡️', hex: '#fbbf24' };
  if (p.includes('tuberculosis')) return { color: 'text-cyan-400', emoji: '🔬', hex: '#22d3ee' };
  if (p.includes('normal')) return { color: 'text-emerald-400', emoji: '✅', hex: '#10b981' };

  return { color: 'text-violet-400', emoji: '❓', hex: '#a78bfa' };

}
